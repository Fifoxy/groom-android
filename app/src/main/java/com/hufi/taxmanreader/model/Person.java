package com.hufi.taxmanreader.model;

public class Person {
    private String first_name;
    private String last_name;
    private Integer id;
    private String email;

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public Integer getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }
}
